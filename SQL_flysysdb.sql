-- CRIANDO TABELA AERONAVE
CREATE TABLE IF NOT EXISTS `AERONAVE`(
	`AERONAVE_ID` INT UNSIGNED NOT NULL AUTO_INCREMENT,
	`MARCA` VARCHAR(255) NOT NULL,
    `MODELO` VARCHAR(255) NOT NULL,
	`COR` VARCHAR(255) NOT NULL,
	`TIPO` VARCHAR(255) NOT NULL,
	`PREFIXO` VARCHAR(6) NOT NULL,
    `CAPACIDADE` INT UNSIGNED NOT NULL
    `COMPANHIA_ID` INT UNSIGNED NOT NULL
	PRIMARY KEY (`AERONAVE_ID`),
	UNIQUE INDEX `AERONAVE_ID_UNIQUE` (`AERONAVE_ID`ASC ))
	ENGINE = InnoDB;
