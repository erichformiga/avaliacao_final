-- CRIANDO TABELA PEDIDO
CREATE TABLE IF NOT EXISTS `PEDIDO`(
	`PEDIDO_ID` INT UNSIGNED NOT NULL AUTO_INCREMENT,
	`CLIENTE_ID` SMALLINT(5),
    `PRODUTO_ID` SMALLINT(5),
    `QTDE_PRODUTO` DECIMAL(4,2) NOT NULL,
    `DATA_PEDIDO` DATETIME,
    `VALOR_PEDIDO` DECIMAL(4,2) NOT NULL,
	PRIMARY KEY (`PEDIDO_ID`),
	UNIQUE INDEX `PEDIDO_ID_UNIQUE` (`PEDIDO_ID`ASC ))
	ENGINE = InnoDB;
